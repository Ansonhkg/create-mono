export const utils = {
  /**
   * @param {number} a
   * @param {number} b
   * @returns {number}
   * @example
   * add(1, 2) // 3
   **/
  add: (a, b) => a + b,

  /**
   * @param {number} a
   * @param {number} b
   * @returns {number}
   * @example
   * subtract(2, 1) // 1
   **/
  subtract: (a, b) => a - b,
};
